package edu.uob;

public class GameEntity {
    private String id;
    private String description;

    public void setId(String id){
        this.id = id;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getId() {
        return id;
    }

    public String getDescription() {
        return description;
    }
}
